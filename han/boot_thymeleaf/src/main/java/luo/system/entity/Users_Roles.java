package luo.system.entity;

import javax.persistence.Column;

public class Users_Roles {

    @Column
    private Long userId;
    @Column
    private Long roleId;

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public Long getRoleId() {
        return roleId;
    }

    public void setRoleId(Long roleId) {
        this.roleId = roleId;
    }
}
